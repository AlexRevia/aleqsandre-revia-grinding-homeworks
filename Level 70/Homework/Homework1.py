print("2. Numbers from 1 to 10:")
for i in range(1, 11):
    print(i)
print()

# 2
print("3. Even numbers from 2 to 20:")
num = 2
while num <= 20:
    print(num)
    num += 2
print()

# 3
print("4. Numbers from 10 down to 1:")
for i in range(10, 1 - 1, -1):
    print(i)
print()

# 4
print("5. First 5 multiples of 3:")
count = 1
while count <= 5:
    print(count * 3)
    count += 1
print()

# 5
print("6. Each character in the string:")
text = "Hello"
for char in text:
    print(char)
